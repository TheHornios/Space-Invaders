package marcianitos;

public class Marcianos {
	private int ejeX;
	private int ejeY;
	private boolean vida;
	public Marcianos(){
		
	}
	public void setEjeX(int ejeX){
		this.ejeX = ejeX;
	}
	public void setEjeY(int ejeY){
		this.ejeY = ejeY;
	}
	public int getEjeX(){
		return ejeX;
	}
	public int getEjeY(){
		return ejeY;
	}
	public boolean getVidas(){
		return vida;
	}
}
